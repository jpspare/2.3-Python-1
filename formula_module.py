
# Function 1 - Calculate sqft
def find_house_area(length, width):
    """Given the inputs of length and width, function returns the area of the house (or any rectangle)"""
    area = length * width
    return area

# Function 2 - Calculate Circumference of a Circle

def find_circle_circ(radius):
    """
    Function returns the circumference of a circle given an input radius, including
    importing the value of pi from math.
    """
    from math import pi
    circ = 2 * pi * radius
    return circ




